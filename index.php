                        <h3>SEJARAH</h3>
                        <p class="sejarah mb-5">Sekolah Menengah Kejuruan Insan Sarjana Farmasi Indonesia Banjarmasin disingkat SMK ISFI (SMF) Banjarmasin adalah merupakan satu-satunya Lembaga Pendidikan Tenaga Farmasi Menengah (Asisten Apoteker) di Kawasan Kalimantan (meliputi Kalsel, Kalteng, Kaltim, dan Kalbar)<br>
                        Sekolah Menengah Kejuruan ISFI Banjarmasin ini berstatus Swasta di bawah naungan Yayasan Pembangunan ISFI Kalsel dengan akta Notaris nmr 35 Tanggal 19 Nopember 1991. Saat ini telah terakreditasi oleh Pusdiknakes Depkes RI dengan status <b>akreditasi A</b>
                        Sekolah sendiri dibuka tanggal 1 September 1965 dengan nama Sekolah Asisten Apoteker ikatan Sarjana Farmasi Indonesia (SAA ISFI) yang diresmikan oleh bapak Pengawas / Kepala Dinas Kesehatan Provinsi Kalimantan Selatan
                        Sekolah ini hingga usianya yang ke 53 telah dipimpin oleh 7 (tujuh) orang Kepala Sekolah.
                        <br>
                        Hingga kini SMK ISFI (SMF) Banjarmasin telah banyak menghasilkan lulusan yang tersebar di Kalsel, Kaltim dan Kalteng, karena itu SMK 1SFI (SMF) Banjarmasin sudah merupakan aset daerah Kalsel di bidang pendidikan karena telah ikut berpartisipasi membantu pemerintah mencerdaskan bangsa, khususnya di bidang kefarmasian.
